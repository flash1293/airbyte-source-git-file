def rmdir(directory):
    directory = Path(directory)
    for item in directory.iterdir():
        if item.is_dir():
            rmdir(item)
        else:
            item.unlink()
    directory.rmdir()

def safeRead(blob):
    try:
        return cast(str, blob.read().decode('UTF-8'))
    except UnicodeDecodeError:
        return ""



